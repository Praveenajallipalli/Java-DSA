public class UnaryOperators {
    public static void main(String[] args) {
        int a=10;
        int b=a++;
        System.out.println(a+" "+b);
        a=10;
        b=++a;
        System.out.println(a+" "+b);
        a=10;
        b=--a;
        System.out.println(a+" "+b);
        a=10;
        b=a--;
        System.out.println(a+" "+b);
        
    }
}
